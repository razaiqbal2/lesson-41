# rectangle
import turtle

screen = turtle.Screen()
screen.bgcolor("hotpink")


turtle.title("rectangle")
t = turtle.Turtle()

sc=turtle.Screen()
sc.setup(800,600)


t.speed(3)


for x in range(2):
    t.forward(200)
    t.left(90)
    t.forward(100)
    t.left(90)
    





t.hideturtle()


turtle.done()   
